GO
Create Procedure [dbo].[spAuthenticateUser]
@UserName nvarchar(100),
@Password nvarchar(100)
as
Begin
	Declare @Count int

	Select @Count = COUNT(Username) FROM dbo.Requester
	where[Username] = @UserName and [Password] = @Password

	if(@Count = 1)
	Begin
		Select 1 as ReturnCode
	End
	Else
	Begin
		Select -1 as ReturnCode
	End
End
GO
